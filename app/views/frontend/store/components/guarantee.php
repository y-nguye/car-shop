<h1 class="text-center mb-5">NHỮNG GÌ ĐƯỢC BẢO HÀNH</h1>

<h5>1. THỜI HẠN BẢO HÀNH</h5>
<p>
    - Đây là chế độ bảo hành chung được áp dụng cho tất cả các loại xe du lịch CAR-SHOP AUTO phân phối tại thị trường Việt Nam với thời gian là 36 tháng và không giới hạn số km (riêng các mẫu xe sedans hạng A là 36 tháng hoặc 100.000km tùy vào điều kiện nào đến trước)
    <br />
    - CAR-SHOP AUTO sẽ bảo hành bình ắc quy trong thời hạn 12 tháng (hoặc 20.000 km tùy điều kiện nào đến trước).
    <br />
    - CAR-SHOP AUTO sẽ bảo hành nạp lại ga lạnh trong 12 tháng (hoặc 20.000km tùy điều kiện nào đến trước).
    <br />
    - Bảo hành lốp do nhà sản xuất lốp tại Việt Nam hoặc có đại diện tại Việt Nam bảo hành theo chính sách bảo hành của nhà sản xuất lốp.
</p>

<h5>2. ĐIỀU KIỆN BẢO HÀNH</h5>
<p>
    CAR-SHOP AUTO chỉ đảm bảo sửa chữa, thay thế các phụ tùng của xe mới bị hỏng hóc trong điều kiện:
    <br />
    - Xe hoạt động trong điều kiện bình thường.
    <br />
    - Chất lượng phụ tùng không tốt.
    <br />
    - Lỗi lắp ráp.
    <br />
    - Trừ những điều kiện ghi trong mục HẠN CHẾ BẢO HÀNH.
    <br />
    <b>Chú ý</b>: Bảo hành vẫn áp dụng khi xe được chuyển nhượng cho những chủ xe khác.
</p>

<h5>3. PHẠM VI ÁP DỤNG BẢO HÀNH</h5>
<p>
    Bảo hành chỉ áp dụng trong phạm vi nước Cộng hòa Xã hội chủ nghĩa Việt Nam.
</p>

<h5>4. BẢO HÀNH MIỄN PHÍ</h5>
<p class="mb-5">
    Mọi sửa chữa thuộc chế độ bảo hành (phụ tùng, công lao động) là miễn phí.
</p>

<h1 class="text-center mb-5">CÁC TRƯỜNG HỢP KHÔNG ĐƯỢC BẢO HÀNH</h1>
<h5>1. NHỮNG YẾU TỐ NGOÀI KIỂM SOÁT CỦA NHÀ SẢN XUẤT</h5>
<p>
    - Những ảnh hưởng do hạn chế của luật pháp hay quy định của chính phủ.
    <br />
    - Chiến tranh, bạo loạn, đóng cửa nhà máy, lỗi lao động.
    <br />
    - Những sửa chữa hay điều chỉnh do sử dụng sai (đua xe, chở quá tải), tai nạn, cẩu thả, tự ý sửa đổi, tự ý cải tiến, đấu nối, tháo ngắt, sửa chữa, điều chỉnh không đúng kỹ thuật, tự ý lắp thêm phụ tùng/phụ kiện không thuộc chế độ bảo hành.
    <br />
    - Ăn mòn do hóa mỹ phẩm hoặc ăn mòn bề mặt xe do bị đá bắn hoặc xước sơn không được bảo hành.
    <br />
    - Hư hại hay bị ăn mòn do môi trường như mưa axit, hóa chất, nhựa cây, muối, mưa đá, mưa bão, sấm chớp, ngập lụt và những tác động tự nhiên khác đều không được bảo hành.
</p>

<h5>2. KHÔNG BẢO HÀNH XE DÙNG SAI NGUYÊN LIỆU, DẦU BÔI TRƠN</h5>
<p>
    Các trường hợp xe hư hỏng do dùng sai nhiên liệu, dầu bôi trơn không đúng với các loại nguyên liệu đã quy định trong cuốn "Hướng dẫn sử dụng" đều không thuộc chế độ bảo hành.
</p>

<h5>3. CHI PHÍ BẢO DƯỠNG THUỘC TRÁCH NHIỆM CỦA CHỦ XE</h5>
<p>
    Những công việc hiệu chỉnh động cơ, thay dầu bôi trơn, rửa xe, đánh bóng, thay bầu lọc, nước làm mát, dung dịch điện phân, ga điều hòa, nhiên liệu, các loại dầu mỡ, chất phụ gia, bugi, cầu chì, bóng đèn (trừ bóng halogen, bóng HID, bóng liền chóa), dây đai dẫn động (trừ dây đai cam), cao su gạt nước, má phanh, đĩa côn đã mòn là việc bảo dưỡng thường xuyên yêu cầu cho mọi loại xe…đều là những hạng mục bảo dưỡng không thuộc chế độ bảo hành.
</p>

<h5>4. TIẾNG ĐỘNG BÌNH THƯỜNG, XE RUNG, GIẢM GIÁ TRỊ TỰ NHIÊN</h5>
<p>
    Tiếng động bình thường, xe rung, sự ăn mòn hay bị giảm giá trị tự nhiên như ngả màu, biến dạng, tỳ vết, không được bảo hành.
</p>

<h5>5. CHI PHÍ PHỤ</h5>
<p class="mb-5">
    Thiệt hại phụ hoặc hậu quả kèm theo như gọi điện thoại, mất thời gian, nhỡ việc hay thiệt hại về thương mại không thuộc chế độ bảo hành.
</p>

<h1 class="text-center mb-5">TRÁCH NHIỆM CỦA CHỦ XE</h1>
<p>
    Trong điều kiện giới hạn bảo hành phụ tùng phụ kiện, khách hàng phải có trách nhiệm:
    <br />
    - Sử dụng, bảo dưỡng xe đúng theo hướng dẫn trong sổ “Hướng Dẫn Sử Dụng” xe.
    <br />
    - Lưu giữ tài liệu liên quan đến việc thực hiện bảo dưỡng định kỳ theo qui định trong Sổ Bảo Hành, vì đó là tài liệu chứng minh xe của bạn đã thực hiện bảo dưỡng định kỳ theo qui định.
    <br />
    - Lưu giữ cẩn thận các tài liệu, lệnh sửa chữa, hóa đơn liên quan đến việc thay thế phụ tùng vì đó là tài liệu chứng minh xe của bạn được thay thế phụ tùng do CAR-SHOP cung cấp.
</p>